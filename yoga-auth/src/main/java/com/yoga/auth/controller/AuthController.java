import java.util.concurrent.TimeUnit;

@Api(tags = "认证中心")
@RestController
@AllArgsConstructor
@RequestMapping("/oauth")
@Slf4j
public class AuthController {

    private TokenEndpoint tokenEndpoint;

    @Autowired
    private RedisService redisService;

    private KeyPair keyPair;

    @ApiOperation(value = "OAuth2认证", notes = "登录入口")
    @ApiImplicitParams({
            @ApiImplicitParam(paramType = "query", name = "grant_type", defaultValue = "password", value = "授权模式",dataType = "String",required = true),
            @ApiImplicitParam(paramType = "query",name = "client_id", defaultValue = "client", value = "Oauth2客户端ID",dataType = "String", required = true),
            @ApiImplicitParam(paramType = "query",name = "client_secret", defaultValue = "123456", value = "Oauth2客户端秘钥",dataType = "String", required = true),
            @ApiImplicitParam(paramType = "query",name = "refresh_token", value = "刷新token",dataType = "String"),
            @ApiImplicitParam(paramType = "query",name = "username", defaultValue = "admin",dataType = "String", value = "用户名"),
            @ApiImplicitParam(paramType = "query",name = "password", defaultValue = "123456", dataType = "String",value = "用户密码")
    })
    @PostMapping("/token")
    public Object postAccessToken(
            @ApiIgnore Principal principal,
            @ApiIgnore @RequestParam Map<String, String> parameters
    ) throws HttpRequestMethodNotSupportedException {

        /**
         * 获取登录认证的客户端ID
         *
         * 兼容两种方式获取Oauth2客户端信息（client_id、client_secret）
         * 方式一：client_id、client_secret放在请求路径中(注：当前版本已废弃)
         * 方式二：放在请求头（Request Headers）中的Authorization字段，且经过加密，例如 Basic Y2xpZW50OnNlY3JldA== 明文等于 client:secret
         */
        String clientId = RequestUtils.getOAuth2ClientId();
        log.info("OAuth认证授权 客户端ID:{}，请求参数：{}", clientId, JSONUtil.toJsonStr(parameters));

        /**
         * knife4j接口文档测试使用
         *
         * 请求头自动填充，token必须原生返回，不能有任何包装，否则显示 undefined undefined
         * 账号/密码:  client_id/client_secret : client/123456
         */
//        if (SecurityConstants.TEST_CLIENT_ID.equals(clientId)) {
//            return tokenEndpoint.postAccessToken(principal, parameters).getBody();
//        }

        OAuth2AccessToken accessToken = tokenEndpoint.postAccessToken(principal, parameters).getBody();
        return Result.success(accessToken);
    }

    @ApiOperation(value = "注销")
    @DeleteMapping("/logout")
    public Result logout() {
        JSONObject payload = JwtUtils.getJwtPayload();
        String jti = payload.getStr(SecurityConstants.JWT_JTI); // JWT唯一标识
        Long expireTime = payload.getLong(SecurityConstants.JWT_EXP); // JWT过期时间戳(单位：秒)
        if (expireTime != null) {
            long currentTime = System.currentTimeMillis() / 1000;// 当前时间（单位：秒）
            if (expireTime > currentTime) { // token未过期，添加至缓存作为黑名单限制访问，缓存时间为token过期剩余时间
                redisService.setCacheObject(SecurityConstants.TOKEN_BLACKLIST_PREFIX + jti, null, (expireTime - currentTime), TimeUnit.SECONDS);
            }
        } else { // token 永不过期则永久加入黑名单
            redisService.setCacheObject(SecurityConstants.TOKEN_BLACKLIST_PREFIX + jti, null);
        }
        return Result.success("注销成功");
    }

    @ApiOperation(value = "获取公钥")
    @GetMapping("/public-key")
    public Map<String, Object> getPublicKey() {
        RSAPublicKey publicKey = (RSAPublicKey) keyPair.getPublic();
        RSAKey key = new RSAKey.Builder(publicKey).build();
        return new JWKSet(key).toJSONObject();
    }

}
